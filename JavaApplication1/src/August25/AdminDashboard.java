    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String uname = jTextField1.getText();
        String pass  = jPasswordField1.getText();
        String gender = null;
        if(jRadioButton1.isSelected())
        {
            gender = "Male";      
        }
        else if(jRadioButton2.isSelected())
        {
            gender = "Female";
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Select Gender");
        }
        int contact = Integer.parseInt(jTextField2.getText());
        String addr = jTextArea1.getText();
        
           Class.forName("com.mysql.jdbc.Driver");
           //establish connection
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tejas","root","123456");
            PreparedStatement st = con.prepareStatement("insert into librarians value(?,?,?,?,?)");
            
            //set values for parameters
            st.setString(1, uname);
            st.setString(2, pass);
            st.setString(3, gender);
            st.setInt(4, contact);
            st.setString(5, addr);
            
            //execute statement
            int x = st.executeUpdate();
            
            if(x>0)
            {
                JOptionPane.showMessageDialog(null, "Librarian Added");
                jTextField1.setText("");
                jPasswordField1.setText("");
                jRadioButton1.setSelected(false);
                jRadioButton2.setSelected(false);
                jTextField2.setText("");
                jTextArea1.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "An error occured");
            }
            //close all connections
            st.close();
            con.close();
       }
       catch(Exception e)
       {
           System.out.println(e);
       }
        
    }//GEN-LAST:event_jButton1ActionPerformed

