public static int delete(int contact){
		int status=0;
		try{
			Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tejas","root","123456");
			PreparedStatement ps=con.prepareStatement("delete from librarians where contact=?");
			ps.setInt(1,contact);
			status=ps.executeUpdate();
			con.close();
		}catch(Exception e)
                {
                    System.out.println(e);
                }
		return status;
	}
